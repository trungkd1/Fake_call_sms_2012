package org.baole.fakelog.model;


public class FileItems {
	
    public static int id_image;
	
	public static int getId_image() {
		return id_image;
	}

	public static void setId_image(int id_image) {
		FileItems.id_image = id_image;
	}


	public String DateSchedule;
	
	public String getDateSchedule() {
		return DateSchedule;
	}

	public void setDateSchedule(String dateSchedule) {
		DateSchedule = dateSchedule;
	}

	public String getNumber() {
		return Number;
	}

	public void setNumber(String number) {
		Number = number;
	}

	public String getMessage() {
		return Message;
	}

	public void setMessage(String message) {
		Message = message;
	}

	public int getRead() {
		return Read;
	}

	public void setRead(int read) {
		Read = read;
	}

	public int getType() {
		return Type;
	}

	public void setType(int type) {
		Type = type;
	}

	public int getDuration() {
		return Duration;
	}

	public void setDuration(int duration) {
		Duration = duration;
	}

	private String Voice;	
	private int Sms_id;
	private int Call_id;
	private String Number;
	private String Message;
	private int Read;
	private int Type;
	private int Duration;		
	private int Nof;
	private String Name;
	
	public String getVoice() {
		return Voice;
	}

	public void setVoice(String voice) {
		Voice = voice;
	}
	
	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public int getNof() {
		return Nof;
	}

	public void setNof(int nof) {
		Nof = nof;
	}

	public FileItems(){
		
	}	
	
	public FileItems(int sms_id,String Name,String Number, String Message, String DateSchedule,int Read, int Type,int Nof){
		this.Sms_id = sms_id;
		this.Name = Name;
		this.Number = Number;
		this.DateSchedule = DateSchedule;
		this.Read = Read;
		this.Type = Type;
		this.Message = Message;
		this.Nof = Nof;
	}
	
	public FileItems(int call_id,String Name,String Number, String DateSchedule,int Duration, int Type,int Nof,String Voice){
		this.Call_id = call_id;
		this.Name = Name;
		this.Number = Number;
		this.DateSchedule = DateSchedule;
		this.Duration = Duration;
		this.Type = Type;
		this.Nof = Nof;
		this.Voice = Voice;
	}

	public int getSms_id() {
		return Sms_id;
	}

	public void setSms_id(int sms_id) {
		Sms_id = sms_id;
	}

	public int getCall_id() {
		return Call_id;
	}

	public void setCall_id(int call_id) {
		Call_id = call_id;
	}
}


